// Integer types
int a = 10; // Regular integer
long long b = 10000000000; // larger integer
unsigned int c = 42; // Non-negative integer
short d = 30000; // Smaller integer type

// Character types
char ch = 'A'; // Single character
wchar_t wc = L'B'; // Wide character (for unicode)

// Boolean type
bool e = true; // Boolean value

// Floating-point types
float f = 3.14f; // Single precision floating-point
double g = 3.141592653589; // Double precision floating-point
long double h = 3.14159265358979323846L; // Extended-precision floating-point

// Special type
void* ptr = nullptr; // void pointer (points to no type)
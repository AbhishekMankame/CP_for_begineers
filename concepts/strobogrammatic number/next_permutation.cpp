// What is next_permutation?
/*next_permutation is a function in the C++ STL (in the <algorithm>
header) that rearranges the elements in a given range to the next lexicographically greater permutation of the elements.
- If such a permutation exists, it transforms the range into the next permutation and returns true.
- If the range is already in the highest possible permutaion (i.e, sorted in descending order), it rearranges the elements into the lowest possible permutation (sorted in ascending ordre) and returns false.
*/